export function render(text: string) {
	const lines = ['', '', '', ''];
	for (const char of text.toLowerCase()) {
		if (alphabet[char as keyof typeof alphabet]) {
			const char_lines = alphabet[char as keyof typeof alphabet];
			for (let i = 0; i < char_lines.length; i++) {
				lines[i] += char_lines[i] + ' ';
			}
		}
	}
	return lines.join('\n');
}
